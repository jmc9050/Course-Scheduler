    private void displayClassesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayClassesButtonActionPerformed
        // TODO add your handling code here:
        ArrayList<ClassDescription> classes = MultiTableQueries.getAllClassDescriptions(currentSemester);
        DefaultTableModel displayClassesTableModel = (DefaultTableModel) displayClassesTable.getModel();
        
        displayClassesTableModel.setNumRows(0);
        Object[] rowData = new Object[3];
        
        for(ClassDescription c: classes){
            rowData[0] = c.getCourseCode();
            rowData[1] = c.getDescription();
            rowData[2] = c.getSeats();
            displayClassesTableModel.addRow(rowData);
        }
    }//GEN-LAST:event_displayClassesButtonActionPerformed

    private void scheduleClassSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleClassSubmitButtonActionPerformed
        // TODO add your handling code here:
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        String status;
        if(ScheduleQueries.getScheduledStudentCount(currentSemester, (String)selectClassComboBox.getSelectedItem()) >= ClassQueries.getClassSeats(currentSemester, (String)selectClassComboBox.getSelectedItem())){
            status = "W";
        }
        else{
            status = "S";
        }
        StudentEntry id = (StudentEntry)selectStudentComboBox.getSelectedItem();
        ScheduleEntry schedule = new ScheduleEntry(currentSemester, (String)selectClassComboBox.getSelectedItem(), id.getStudentID(), status, currentTimestamp);
        ScheduleQueries.addScheduleEntry(schedule);
        if(status == "S")
        {
            scheduleClassStatusLabel.setText(id + " has been scheduled");
        }
        else
        {
            scheduleClassStatusLabel.setText(id + " has been waitlisted");
        }
    }//GEN-LAST:event_scheduleClassSubmitButtonActionPerformed


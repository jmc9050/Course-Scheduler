    private void dropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentButtonActionPerformed
        // TODO add your handling code here:
        dropStudentTextArea.append((StudentEntry)dropStudentComboBox.getSelectedItem() + " has been dropped from the list of students.\n");
        StudentEntry st = (StudentEntry)dropStudentComboBox.getSelectedItem();
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        for(String s: semesters)
        {
            dropStudentTextArea.append("\nFor Semester: " + s + "\n");
            ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduleByStudent(s, st.getStudentID());
            if(schedule.size() > 0)
            {
                for(ScheduleEntry sc: schedule)
                {
                    ArrayList<ScheduleEntry> wStudent = ScheduleQueries.getWaitlistedStudentsByClass(s, sc.getCourseCode());
                    ArrayList<StudentEntry> temp = MultiTableQueries.getWaitlistedStudentsByClass(s, sc.getCourseCode());
                    dropStudentTextArea.append(st + " has been dropped from " + sc.getCourseCode() + "\n");
                    if(temp.size() > 0 && sc.getStatus() != "W")
                    {
                        dropStudentTextArea.append(temp.get(0) + " has been scheduled into " + sc.getCourseCode() + "\n");
                        ScheduleQueries.updateScheduleEntry(wStudent.get(0));
                    }
                    ScheduleQueries.dropStudentScheduleByCourse(s, st.getStudentID(), sc.getCourseCode());
                }
            }
        }
        StudentQueries.dropStudent(st.getStudentID());
        rebuildStudentComboBoxes();
    }//GEN-LAST:event_dropStudentButtonActionPerformed

